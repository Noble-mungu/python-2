class Source:
	'''
	Source class to define source objects
	'''
	def __init__(self,id,name,description,url,category):
		'''
		Function to initialize source Objects
		Args:
		1.id
		2.name
		3.description
		4.url
		5.category
		'''
		self.id = id 
		self.name = name
		self.description = description
		self.url = url
		self.category = category
		

